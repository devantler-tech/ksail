func TestRenderSortsAndDropsEmptyListEntries(t *testing.T) {
	t.Parallel()

	stream, err := kubeadmbootstrap.Render(kubeadmbootstrap.NodeConfig{
		Role:     kubeadmbootstrap.RoleServerInit,
		Token:    "abcdef.0123456789abcdef",
		CertSANs: []string{"z.example.com", "", "a.example.com"},
	})
	require.NoError(t, err)

	docs := decodeDocs(t, stream)
	require.Len(t, docs, 2)

	var clusterCfg parsedCluster

	require.NoError(t, docs[1].Decode(&clusterCfg))
	require.NotNil(t, clusterCfg.APIServer)
	assert.Equal(t, []string{"a.example.com", "z.example.com"}, clusterCfg.APIServer.CertSANs,
		"cert SANs are sorted and empty entries dropped for deterministic output")
}

func TestRenderJoinSortsCACertHashes(t *testing.T) {
	t.Parallel()

	hashA := "sha256:" + strings.Repeat("a", 64)
	hashB := "sha256:" + strings.Repeat("b", 64)

	stream, err := kubeadmbootstrap.Render(kubeadmbootstrap.NodeConfig{
		Role:              kubeadmbootstrap.RoleAgent,
		Token:             "abcdef.0123456789abcdef",
		APIServerEndpoint: "10.0.0.1:6443",
		CACertHashes:      []string{hashB, hashA},
	})
	require.NoError(t, err)

	docs := decodeDocs(t, stream)

	var joinCfg parsedJoin

	require.NoError(t, docs[0].Decode(&joinCfg))

	want := []string{hashA, hashB}
	assert.Equal(t, want, joinCfg.Discovery.BootstrapToken.CACertHashes,
		"CA cert hashes are sorted for deterministic output")
}

//nolint:funlen // table-driven test enumerating the validation cases.
func TestRenderRejectsInvalidConfigs(t *testing.T) {
	t.Parallel()

	cases := map[string]struct {
		cfg     kubeadmbootstrap.NodeConfig
		wantErr error
	}{
		"invalid role": {
			cfg:     kubeadmbootstrap.NodeConfig{Role: "bootstrapper", Token: "t"},
			wantErr: kubeadmbootstrap.ErrInvalidRole,
		},
		"missing token": {
			cfg:     kubeadmbootstrap.NodeConfig{Role: kubeadmbootstrap.RoleServerInit},
			wantErr: kubeadmbootstrap.ErrMissingToken,
		},
		"server-init with join fields": {
			cfg: kubeadmbootstrap.NodeConfig{
				Role:              kubeadmbootstrap.RoleServerInit,
				Token:             "t",
				APIServerEndpoint: "10.0.0.1:6443",
			},
			wantErr: kubeadmbootstrap.ErrServerInitWithJoinFields,
		},
		"join missing endpoint": {
			cfg: kubeadmbootstrap.NodeConfig{
				Role:         kubeadmbootstrap.RoleAgent,
				Token:        "t",
				CACertHashes: []string{validCACertHash},
			},
			wantErr: kubeadmbootstrap.ErrMissingAPIServerEndpoint,
		},
		"join invalid endpoint (no port)": {
			cfg: kubeadmbootstrap.NodeConfig{
				Role:              kubeadmbootstrap.RoleAgent,
				Token:             "t",
				APIServerEndpoint: "10.0.0.1",
				CACertHashes:      []string{validCACertHash},
			},
			wantErr: kubeadmbootstrap.ErrInvalidAPIServerEndpoint,
		},
		"join invalid endpoint (non-numeric port)": {
			cfg: kubeadmbootstrap.NodeConfig{
				Role:              kubeadmbootstrap.RoleAgent,
				Token:             "t",
				APIServerEndpoint: "10.0.0.1:https",
				CACertHashes:      []string{validCACertHash},
			},
			wantErr: kubeadmbootstrap.ErrInvalidAPIServerEndpoint,
		},
		"join missing CA hashes": {
			cfg: kubeadmbootstrap.NodeConfig{
				Role:              kubeadmbootstrap.RoleServer,
				Token:             "t",
				APIServerEndpoint: "10.0.0.1:6443",
			},
			wantErr: kubeadmbootstrap.ErrMissingCACertHashes,
		},
		"join invalid CA hash (no prefix)": {
			cfg: kubeadmbootstrap.NodeConfig{
				Role:              kubeadmbootstrap.RoleServer,
				Token:             "t",
				APIServerEndpoint: "10.0.0.1:6443",
				CACertHashes:      []string{"deadbeef"},
			},
			wantErr: kubeadmbootstrap.ErrInvalidCACertHash,
		},
		"join invalid CA hash (non-hex digest)": {
			cfg: kubeadmbootstrap.NodeConfig{
				Role:              kubeadmbootstrap.RoleServer,
				Token:             "t",
				APIServerEndpoint: "10.0.0.1:6443",
				CACertHashes:      []string{"sha256:" + strings.Repeat("z", 64)},
			},
			wantErr: kubeadmbootstrap.ErrInvalidCACertHash,
		},
		"join invalid CA hash (too short)": {
			cfg: kubeadmbootstrap.NodeConfig{
				Role:              kubeadmbootstrap.RoleServer,
				Token:             "t",
				APIServerEndpoint: "10.0.0.1:6443",
				CACertHashes:      []string{"sha256:deadbeef"},
			},
			wantErr: kubeadmbootstrap.ErrInvalidCACertHash,
		},
		"join with cluster-wide option": {
			cfg: kubeadmbootstrap.NodeConfig{
				Role:              kubeadmbootstrap.RoleAgent,
				Token:             "t",
				APIServerEndpoint: "10.0.0.1:6443",
				CACertHashes:      []string{validCACertHash},
				PodSubnet:         "10.244.0.0/16",
			},
			wantErr: kubeadmbootstrap.ErrServerInitOnlyOption,
		},
	}

	for name, testCase := range cases {
		t.Run(name, func(t *testing.T) {
			t.Parallel()

			_, err := kubeadmbootstrap.Render(testCase.cfg)
			require.ErrorIs(t, err, testCase.wantErr)
		})
	}
}
