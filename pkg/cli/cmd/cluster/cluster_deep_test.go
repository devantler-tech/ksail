func TestRemoveAutoGeneratedJobLabels(t *testing.T) {
	t.Parallel()

	tests := []struct {
		name          string
		labels        map[string]string
		expectLabels  map[string]string
		expectRemoved bool
	}{
		{
			name: "removes batch controller-uid",
			labels: map[string]string{
				"batch.kubernetes.io/controller-uid": "abc123",
				"app":                                "myapp",
			},
			expectLabels: map[string]string{
				"app": "myapp",
			},
		},
		{
			name: "removes controller-uid",
			labels: map[string]string{
				"controller-uid": "abc123",
				"app":            "myapp",
			},
			expectLabels: map[string]string{
				"app": "myapp",
			},
		},
		{
			name: "removes both auto-generated labels",
			labels: map[string]string{
				"batch.kubernetes.io/controller-uid": "abc123",
				"controller-uid":                     "def456",
				"app":                                "myapp",
			},
			expectLabels: map[string]string{
				"app": "myapp",
			},
		},
		{
			name: "removes entire labels field when empty after cleanup",
			labels: map[string]string{
				"batch.kubernetes.io/controller-uid": "abc123",
				"controller-uid":                     "def456",
			},
			expectRemoved: true,
		},
		{
			name:         "no labels to remove",
			labels:       map[string]string{"app": "myapp"},
			expectLabels: map[string]string{"app": "myapp"},
		},
	}

	for _, testCase := range tests {
		t.Run(testCase.name, func(t *testing.T) {
			t.Parallel()

			obj := &unstructured.Unstructured{Object: map[string]any{
				"metadata": map[string]any{
					"labels": convertLabels(testCase.labels),
				},
			}}

			cluster.ExportRemoveAutoGeneratedJobLabels(obj, "metadata", "labels")

			if testCase.expectRemoved {
				labels, found, _ := unstructured.NestedStringMap(obj.Object, "metadata", "labels")
				assert.True(t, !found || len(labels) == 0)
			} else if testCase.expectLabels != nil {
				labels, _, _ := unstructured.NestedStringMap(obj.Object, "metadata", "labels")
				assert.Equal(t, testCase.expectLabels, labels)
			}
		})
	}
}

func convertLabels(m map[string]string) map[string]any {
	result := make(map[string]any, len(m))
	for k, v := range m {
		result[k] = v
	}

	return result
}

