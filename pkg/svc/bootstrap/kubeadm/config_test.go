// validCACertHash is a full SHA-256 CA pin (64 hex chars), the form kubeadm's
// token discovery requires, used as a valid fixture.
const validCACertHash = "sha256:" +
	"0123456789abcdef0123456789abcdef0123456789abcdef0123456789abcdef"

// parsedInit mirrors the InitConfiguration subset Render emits.
type parsedInit struct {
	APIVersion      string `yaml:"apiVersion"`
	Kind            string `yaml:"kind"`
	BootstrapTokens []struct {
		Token string `yaml:"token"`
	} `yaml:"bootstrapTokens"`
}

// parsedCluster mirrors the ClusterConfiguration subset Render emits.
type parsedCluster struct {
	APIVersion           string `yaml:"apiVersion"`
	Kind                 string `yaml:"kind"`
	KubernetesVersion    string `yaml:"kubernetesVersion"`
	ControlPlaneEndpoint string `yaml:"controlPlaneEndpoint"`
	Networking           *struct {
		PodSubnet     string `yaml:"podSubnet"`
		ServiceSubnet string `yaml:"serviceSubnet"`
	} `yaml:"networking"`
	APIServer *struct {
		CertSANs []string `yaml:"certSANs"` //nolint:tagliatelle // kubeadm's schema mandates the key "certSANs".
	} `yaml:"apiServer"`
}

// parsedJoin mirrors the JoinConfiguration subset Render emits.
type parsedJoin struct {
	APIVersion string `yaml:"apiVersion"`
	Kind       string `yaml:"kind"`
	Discovery  struct {
		BootstrapToken struct {
			APIServerEndpoint string   `yaml:"apiServerEndpoint"`
			Token             string   `yaml:"token"`
			CACertHashes      []string `yaml:"caCertHashes"`
		} `yaml:"bootstrapToken"`
	} `yaml:"discovery"`
	ControlPlane *struct{} `yaml:"controlPlane"`
}

// decodeDocs asserts the stream carries the managed-file header and decodes its
// YAML documents into nodes, so tests assert on parsed documents rather than on
// brittle substrings.
func decodeDocs(t *testing.T, stream string) []yaml.Node {
	t.Helper()

	require.True(t,
		strings.HasPrefix(stream, "# Generated by ksail — kubeadm"),
		"stream must begin with the managed-file header",
	)

	decoder := yaml.NewDecoder(strings.NewReader(stream))

	var nodes []yaml.Node

	for {
		var node yaml.Node

		err := decoder.Decode(&node)
		if errors.Is(err, io.EOF) {
			break
		}

		require.NoError(t, err, "every document must be valid YAML")

		nodes = append(nodes, node)
	}

	return nodes
}

func TestRenderServerInit(t *testing.T) {
	t.Parallel()

	stream, err := kubeadmbootstrap.Render(kubeadmbootstrap.NodeConfig{
		Role:                 kubeadmbootstrap.RoleServerInit,
		Token:                "abcdef.0123456789abcdef",
		KubernetesVersion:    "v1.31.0",
		ControlPlaneEndpoint: "k8s.example.com:6443",
		CertSANs:             []string{"k8s.example.com"},
		PodSubnet:            "10.244.0.0/16",
		ServiceSubnet:        "10.96.0.0/12",
	})
	require.NoError(t, err)

	docs := decodeDocs(t, stream)
	require.Len(t, docs, 2, "the initialising control plane renders Init + Cluster configurations")

	var initCfg parsedInit

	require.NoError(t, docs[0].Decode(&initCfg))
	assert.Equal(t, "kubeadm.k8s.io/v1beta4", initCfg.APIVersion)
	assert.Equal(t, "InitConfiguration", initCfg.Kind)
	require.Len(t, initCfg.BootstrapTokens, 1)
	assert.Equal(t, "abcdef.0123456789abcdef", initCfg.BootstrapTokens[0].Token)

	var clusterCfg parsedCluster

	require.NoError(t, docs[1].Decode(&clusterCfg))
	assert.Equal(t, "ClusterConfiguration", clusterCfg.Kind)
	assert.Equal(t, "v1.31.0", clusterCfg.KubernetesVersion)
	assert.Equal(t, "k8s.example.com:6443", clusterCfg.ControlPlaneEndpoint)
	require.NotNil(t, clusterCfg.Networking)
	assert.Equal(t, "10.244.0.0/16", clusterCfg.Networking.PodSubnet)
	assert.Equal(t, "10.96.0.0/12", clusterCfg.Networking.ServiceSubnet)
	require.NotNil(t, clusterCfg.APIServer)
	assert.Equal(t, []string{"k8s.example.com"}, clusterCfg.APIServer.CertSANs)
}

func TestRenderServerInitMinimalOmitsEmptyClusterKeys(t *testing.T) {
	t.Parallel()

	stream, err := kubeadmbootstrap.Render(kubeadmbootstrap.NodeConfig{
		Role:  kubeadmbootstrap.RoleServerInit,
		Token: "abcdef.0123456789abcdef",
	})
	require.NoError(t, err)

	docs := decodeDocs(t, stream)
	require.Len(t, docs, 2)

	var clusterCfg parsedCluster

	require.NoError(t, docs[1].Decode(&clusterCfg))
	assert.Equal(t, "ClusterConfiguration", clusterCfg.Kind)
	assert.Empty(t, clusterCfg.KubernetesVersion, "kubernetes version is omitted when unset")
	assert.Nil(t, clusterCfg.Networking, "the networking block is omitted when no subnet is set")
	assert.Nil(t, clusterCfg.APIServer, "the apiServer block is omitted when no cert SAN is set")
}

func TestRenderServerJoinsAsControlPlane(t *testing.T) {
	t.Parallel()

	stream, err := kubeadmbootstrap.Render(kubeadmbootstrap.NodeConfig{
		Role:              kubeadmbootstrap.RoleServer,
		Token:             "abcdef.0123456789abcdef",
		APIServerEndpoint: "10.0.0.1:6443",
		CACertHashes:      []string{validCACertHash},
	})
	require.NoError(t, err)

	docs := decodeDocs(t, stream)
	require.Len(t, docs, 1, "a joining node renders a single JoinConfiguration")

	var joinCfg parsedJoin

	require.NoError(t, docs[0].Decode(&joinCfg))
	assert.Equal(t, "JoinConfiguration", joinCfg.Kind)
	assert.Equal(t, "10.0.0.1:6443", joinCfg.Discovery.BootstrapToken.APIServerEndpoint)
	assert.Equal(t, "abcdef.0123456789abcdef", joinCfg.Discovery.BootstrapToken.Token)
	assert.Equal(t, []string{validCACertHash}, joinCfg.Discovery.BootstrapToken.CACertHashes)
	assert.NotNil(t, joinCfg.ControlPlane, "an additional server joins as a control plane")
}

